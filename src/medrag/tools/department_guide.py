# 科室导诊关键词
_GUIDE_KEYWORDS = [
    "挂什么科", "看什么科", "哪个科室", "什么科室", "去哪个科",
    "应该挂", "推荐挂", "看哪个科", "挂哪个科",
]

_DEPT_KEYWORDS_PATTERN = re.compile("|".join(_GUIDE_KEYWORDS))


class DepartmentGuide(BaseTool):
    name = "科室导诊"
    description = "根据症状或疾病推荐就诊科室"

    def match(self, query: str) -> Optional[Dict[str, str]]:
        if not _DEPT_KEYWORDS_PATTERN.search(query):
            return None
        # 只有能查到科室才拦截，否则放行给 RAG
        for disease in _DISEASE_DEPT:
            if disease in query:
                return {"query": query}
        for symptom in _SYMPTOM_DEPT:
            if symptom in query:
                return {"query": query}
        return None

    def execute(self, query: str = "") -> str:
        # 先精确匹配疾病
        for disease, dept in _DISEASE_DEPT.items():
            if disease in query:
                return f"【{disease}】建议就诊科室：**{dept}**\n（以上建议仅供参考，请以医院实际分诊为准）"

        # 再匹配症状
        matched = []
        for symptom, dept in _SYMPTOM_DEPT.items():
            if symptom in query:
                matched.append((symptom, dept))

        if matched:
            # 按匹配长度降序（优先更具体的症状）
            matched.sort(key=lambda x: len(x[0]), reverse=True)
            lines = [f"根据您描述的"]
            for symptom, dept in matched:
                lines.append(f"  - 「{symptom}」→ **{dept}**")
            lines.append("（以上建议仅供参考，请以医院实际分诊为准）")
            return "\n".join(lines)

        return (
            "未识别到明确症状或疾病，建议您：\n"
            "1. 到医院的 **导诊台** 咨询\n"
            "2. 如果不知道挂什么科，可以先挂 **全科 / 内科** 初筛\n"
            "3. 急诊请直接前往 **急诊科**"
        )
